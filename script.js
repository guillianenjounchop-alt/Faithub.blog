const verseTextElement = document.getElementById('verse-text');
const closeVerseButton = document.getElementById('close-verse');
const versePopup = document.getElementById('verse-popup');

function showVerseOfTheDay() {
  const randomVerse = verseOfTheDay[Math.floor(Math.random() * verseOfTheDay.length)];
  verseTextElement.innerText = `${randomVerse.verse} - ${randomVerse.text}`;
  versePopup.classList.add('show');
}

closeVerseButton.addEventListener('click', () => {
  versePopup.classList.remove('show');
});
showVerseOfTheDay();
 
function copyLink() {
  const url = window.location.href;
  navigator.clipboard.writeText(url).then(() => {
    alert('Link copied to clipboard!');
  });
}